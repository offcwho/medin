async function ensureAuthorized() {
    const cookieStore = await cookies();
    const token = cookieStore.get('dashboard_access_token')?.value;

    if (!token) {
        redirect('/dashboard/login');
    }

    const apiBaseUrl = (process.env.NEXT_PUBLIC_API_URL ?? 'http://localhost:9000').replace(/\/+$/, '');
    let response: Response;

    try {
        response = await fetch(`${apiBaseUrl}/auth/me`, {
            cache: 'no-store',
            headers: {
                Cookie: `dashboard_access_token=${token}`,
            },
        });
    } catch {
        redirect('/dashboard/login');
    }

    if (!response.ok) {
        redirect('/dashboard/login');
    }
}

export default async function DashboardLayout({
    children,
}: {
    children: React.ReactNode;
}) {
    await ensureAuthorized();

    return (
        <AdminProvider
            routes={adminRoutes}
            apiUrl={process.env.NEXT_PUBLIC_API_URL ?? 'http://localhost:9000'}
            initialAdmin={mockAdmin}
        >
            {children}
        </AdminProvider>
    );
}
